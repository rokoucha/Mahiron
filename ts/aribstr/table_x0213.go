package aribstr

import "unicode/utf8"

func lookupJISX0213(plane byte, first, second byte) (string, bool) {
	if !aribValidRowCell(first, second) {
		return "", false
	}
	key := aribPlaneRowCellKey(plane, first, second)
	if s, ok := jisX0213DeltaDecode[key]; ok {
		return s, true
	}
	if plane == 1 {
		if _, undefined := jisX0213Plane1Undefined[aribRowCellKey(first, second)]; undefined {
			return "", false
		}
		s, err := decodeJISX0208Pair(first, second)
		if err != nil || s == string(utf8.RuneError) {
			return "", false
		}
		return s, true
	}
	return "", false
}

