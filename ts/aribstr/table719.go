func lookupARIBTable719AdditionalSymbol(first, second byte) (string, bool) {
	r, ok := aribTable719AdditionalSymbols[uint16(first)<<8|uint16(second)]
	if !ok {
		return "", false
	}
	if normalized, ok := aribTable719DuplicateUCSCanonical[r]; ok {
		r = normalized
	}
	return string(r), true
}

